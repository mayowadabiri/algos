const decodeMorse = (morse) => {
  const arr = morse.split(" ");
  const morseArr = [];
  let string = "";
  for (let code in MORSE_CODE) {
    morseArr.push({
      code,
      value: MORSE_CODE[code],
    });
  }

  for (let key of arr) {
    const moreseValue = morseArr.find((morse) => morse.code === key);
    string += moreseValue.value;
  }
  console.log(string);
};

